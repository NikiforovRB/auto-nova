function getInitialLanguage(): SupportedLang {
  const saved = localStorage.getItem(STORAGE_KEY) as SupportedLang | null
  if (saved && ['ru', 'en', 'tr', 'sr', 'el'].includes(saved)) return saved
  return 'ru'
}

i18n.use(initReactI18next).init({
  resources,
  lng: getInitialLanguage(),
  fallbackLng: 'ru',
  interpolation: { escapeValue: false },
})

i18n.on('languageChanged', (lng) => {
  localStorage.setItem(STORAGE_KEY, lng)
  // Sync dayjs locale with current language
  const dayjsLocale = lng === 'en' ? 'en' : lng
  dayjs.locale(dayjsLocale)
})

export default i18n

